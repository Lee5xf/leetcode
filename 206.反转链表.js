/**
 * Definition for singly-linked list.
 * function ListNode(val, next) {
 *     this.val = (val===undefined ? 0 : val)
 *     this.next = (next===undefined ? null : next)
 * }
 */
/**
 * @param {ListNode} head
 * @return {ListNode}
 */
var reverseList = function (head) {
    const node = new ListNode(0, null);
    let p = head
    while (p) {
        let r = p.next
        p.next = node.next
        node.next = p
        p = r
    }
    return node.next
};
